        public static int[] GetLetter(int number, int[,,] data)
        {
            int[] result = new int[NumberOfFields];
            for(int i = 0; i < NumberOfFieldsY; i++)
            {
                for(int j = 0; j < NumberOfFieldsX; j++)
                {
                    result[i * NumberOfFieldsX + j] = data[number, i, j];
                }
            }

            return result;
        }
